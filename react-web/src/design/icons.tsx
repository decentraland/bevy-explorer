// Icons backed by the real Unity (unity-explorer) sidebar art in /public/sidebar-icons.
// They're white-on-transparent PNGs drawn as a CSS mask so they take `currentColor` —
// white by default, accent on hover/active (same recolour trick as the backpack icons).
const MASK_ICONS = new Set<IconName>([
  'notifications', 'map', 'communities', 'backpack', 'settings', 'help', 'mic', 'friends', 'chat', 'emotes'
])

export function Icon({
  name,
  size = 22
}: {
  name: IconName
  size?: number
}): React.JSX.Element {
  if (MASK_ICONS.has(name)) {
    const url = `url(/sidebar-icons/${name}.png)`
    return (
      <span
        aria-hidden="true"
        style={{
          display: 'inline-block',
          width: size,
          height: size,
          backgroundColor: 'currentColor',
          maskImage: url,
          WebkitMaskImage: url,
          maskRepeat: 'no-repeat',
          WebkitMaskRepeat: 'no-repeat',
          maskPosition: 'center',
          WebkitMaskPosition: 'center',
          maskSize: 'contain',
          WebkitMaskSize: 'contain'
        }}
      />
    )
  }
  return (
    <svg viewBox="0 0 24 24" width={size} height={size} aria-hidden="true">
      <path d={PATHS[name]} fill="currentColor" />
    </svg>
  )
}
